    private void idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyPressed
        // TODO add your handling code here:
        id1 = Integer.parseInt(id.getText());
        int enter = evt.getKeyCode();
        if(enter==10){
            try{
                sql = "SELECT * FROM pengguna " + "WHERE id='"+id1+"'";
                st=con.createStatement();
                rs=st.executeQuery(sql);
                while(rs.next()){
                    nama.setText(rs.getString("nama"));
                    user.setText(rs.getString("uname"));
                    pwd.setText(rs.getString("pass"));
                    hp.setText(rs.getString("no_hp"));
                    JOptionPane.showMessageDialog(null, "Data Ditemukan");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data Tidak Ada / Tidak Ditemukan" +e.getMessage());
                //nm_brg.requestFocus();
            }
        }
    }//GEN-LAST:event_idKeyPressed

