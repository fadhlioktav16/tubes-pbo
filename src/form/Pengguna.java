    private void ShowData(String sql) {
        DefaultTableModel data = new DefaultTableModel();
        data.addColumn ("ID");
        data.addColumn ("Nama Lengkap");
        data.addColumn("Username");
        data.addColumn ("Password");
        data.addColumn("No Handphone");
        try{
            st=con.createStatement();
            rs = st.executeQuery("SELECT * from pengguna");
            while(rs.next())
                data.addRow(new Object[]{
                    rs.getInt(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                });
            tabelpengguna.setModel(data);
                    }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Gagal Memuat KeDatabase \n Aktifkan Database Sebelum Memulai");
        }
   
    }
