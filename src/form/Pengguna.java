    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        id1 = Integer.parseInt(id.getText());
        fname = String.valueOf(nama.getText());
        username = String.valueOf(user.getText());
        password = String.valueOf(pwd.getText());
        nohp = String.valueOf(hp.getText());
        try{
            sql = "UPDATE pengguna SET nama='"+ fname +"',uname='"+username+"',pass='"+password+"',no_hp='"+nohp+"' WHERE id = '"+ id1 +"'";
            st = con.createStatement();
            st.execute(sql);
            ShowData(sql);
            Clear();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR.");
        }
    }//GEN-LAST:event_editActionPerformed

