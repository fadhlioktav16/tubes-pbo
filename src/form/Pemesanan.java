    private void kd_brgKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_kd_brgKeyReleased
        // TODO add your handling code here:
        kode = kd_brg.getText();
        int enter = evt.getKeyCode();
        if(enter==10){
            try{
                sql = "SELECT * FROM data " + "WHERE kode_barang='"+kode+"'";
                st=con.createStatement();
                rs=st.executeQuery(sql);
                while(rs.next()){
                    nm_kue.setText(rs.getString("nama_kue"));
                    harga.setText(rs.getString("harga"));
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data Tidak Ada / Tidak Ditemukan" +e.getMessage());
                //nm_brg.requestFocus();
            }
        }
    }//GEN-LAST:event_kd_brgKeyReleased

