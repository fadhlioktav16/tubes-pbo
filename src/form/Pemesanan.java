        // TODO add your handling code here:
        id = String.valueOf(no_trans.getText());
        kode = String.valueOf(kd_brg.getText());
        nama = String.valueOf(nm_kue.getText());
        hrg = Integer.parseInt(harga.getText());
        jumlah = Integer.parseInt(jmlh.getText());
        total_harga = Integer.parseInt(total.getText());
       
        pesan.setId(id);
        pesan.setKode(kode);
        pesan.setNama(nama);
